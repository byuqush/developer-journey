# Uyga vazifa

name = " Javlonbek "

print(name)
print(len(name)) # len belgilar sonini ham chiqaradi, bu yerda bo'sh joylar ham hisobga olinadi.

print(name.strip()) # matn boshidagi va oxiridagi bo'sh joylarni olib tashlaydi.

print(len(name.strip())) # matndagi bo'shliqlarini olib tashlagandan keyin belgilar soni

print(name.upper()) # katta harflar bilan yozadi

print(name.lower()) # kichik harflar bilan yozadi

#UYGA_VAZIFA

print(repr(name)) # printdagi bo'sh joylarni aniq ko'rish uchun, repr() obyektni dasturchiga qanday bo'lsa, shunday ko'rsatadi.
print(repr(name.strip())) # printdagi bo'sh joylarsiz ko'rsatish

